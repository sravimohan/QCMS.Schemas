internal static class QuestionType
{
    internal static string String => nameof(String).ToLowerInvariant();
    internal static string Number => nameof(Number).ToLowerInvariant();
    internal static string Integer => nameof(Integer).ToLowerInvariant();
    internal static string Boolean => nameof(Boolean).ToLowerInvariant();
    internal static string Array => nameof(Array).ToLowerInvariant();
    internal static string Textarea => nameof(Textarea).ToLowerInvariant();
}